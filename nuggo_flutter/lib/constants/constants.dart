※ 포트폴리오 공개 기능 이용 시, 회원이 공개 설정한 정보(이름, 경력, 작업물 등)는 해당 URL에 접근하는 불특정 다수에게 공개됩니다. 이는 회원의 명시적 동의(공개 설정 행위)에 따른 것으로, 제3자 제공에 해당합니다. 공개 범위는 언제든지 앱 내 설정에서 변경하실 수 있습니다.

