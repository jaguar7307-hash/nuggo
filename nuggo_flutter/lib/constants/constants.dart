탈퇴 시 처리: 회원 탈퇴 시 공개 URL은 즉시 비활성화되며, 개인정보처리방침에 따라 데이터가 파기됩니다.
타인 정보 포함 금지: 포트폴리오에 제3자(협업자, 고객사 등)의 개인정보나 저작물을 무단으로 포함하는 것은 금지됩니다.

