1-4. NFC 명함 전송

01 NFC 권한 허용
기기 설정에서 NFC가 활성화되어 있는지 확인합니다. (설정 → NFC 및 결제 → 켜기)

02 NFC 태그에 명함 쓰기
명함 상세 화면 → [NFC 쓰기] 버튼 → NFC 태그 카드에 기기 뒷면을 가져다 댑니다.

03 명함 전달
NFC 태그 카드를 상대방 기기에 가져다 대면 명함 정보가 즉시 전달됩니다.

💡 TIP: NFC 태그 카드는 별도 구매가 필요합니다. NUGGO 앱에서 추천 태그 카드를 확인하세요.
⚠ NFC 기능은 NFC를 지원하는 기기에서만 사용 가능합니다. 아이폰은 iOS 13 이상에서 지원됩니다.

1-5. 개인 포트폴리오 공유

01 포트폴리오 만들기
앱 → [포트폴리오] 탭 → [새 포트폴리오 만들기] → 템플릿 선택

02 내용 구성
자기소개, 경력사항, 학력, 작업물 이미지, 외부 링크(GitHub, Behance, 유튜브 등)를 추가합니다.

03 공개 범위 설정
• 전체 공개: 누구나 URL로 열람 가능 (검색엔진 노출 가능)
• 링크 공유: URL을 아는 사람만 열람 가능
• 비공개: 본인만 열람

04 공유하기
생성된 고유 URL 또는 QR코드를 복사하여 이메일, SNS, 메신저로 공유합니다.

💡 TIP: 작업물 이미지는 JPG/PNG 형식, 파일당 최대 [N]MB까지 업로드 가능합니다.
⚠ 타인의 개인정보나 저작물을 동의 없이 포트폴리오에 포함하지 마세요. 이용 제한 사유가 됩니다.
※ '전체 공개' 설정 시 구글·네이버 등 검색엔진에 페이지가 노출될 수 있습니다. 원하지 않으면 '링크 공유' 설정을 이용하세요.

※ QR코드 인쇄 주문 및 명함카드 실물 인쇄 주문 기능은 추후 추가 예정입니다.

1-6. 구독 및 결제 관리

구독 플랜 확인: [마이페이지] → [구독 관리]에서 현재 플랜과 다음 결제일을 확인할 수 있습니다.
구독 해지: 앱스토어(구글 플레이 / 애플 앱스토어) 설정 → 구독 항목에서 NUGGO를 찾아 해지합니다.
결제 수단 변경: 각 앱스토어의 결제 정보 관리 화면에서 변경합니다.

※ 구독 해지 후에도 남은 결제 기간 동안 프리미엄 기능을 계속 이용할 수 있습니다.
※ 자동 갱신 결제 예정일 7일 전에 이메일과 앱 알림으로 사전 안내를 드립니다.

1-7. 계정 및 보안 설정

비밀번호 변경: [마이페이지] → [계정 설정] → [비밀번호 변경]
비밀번호 분실: 로그인 화면 → [비밀번호 찾기] → 가입 이메일로 재설정 링크 발송
알림 설정: [마이페이지] → [알림 설정]에서 마케팅 수신, 결제 알림 등을 개별 조정할 수 있습니다.
회원 탈퇴: [마이페이지] → [계정 설정] → [회원 탈퇴] → 탈퇴 사유 입력 후 확인

⚠ 탈퇴 시 계정, 명함, 포트폴리오 데이터가 즉시 삭제되며 복구가 불가합니다. 탈퇴 전 필요한 데이터를 백업하세요.

CHAPTER 2. 자주 묻는 질문 (FAQ)

📱 앱 설치 및 계정

Q: NUGGO는 어떤 기기에서 사용할 수 있나요?
A: iOS 14 이상의 아이폰/아이패드, Android 8.0 이상의 스마트폰에서 사용 가능합니다.

Q: 소셜 로그인(구글/애플)으로 가입하면 이메일 로그인도 가능한가요?
A: 소셜 로그인과 이메일 로그인은 별도 계정으로 관리됩니다. [계정 설정]에서 이메일 연동을 추가할 수 있습니다.

Q: 계정을 삭제하면 데이터를 복구할 수 있나요?
A: 탈퇴 즉시 모든 데이터가 삭제되며 복구가 불가합니다. 탈퇴 전 필요한 데이터를 내보내기(Export) 기능으로 저장하세요.

💳 구독 및 결제

Q: 무료로 사용할 수 있는 기능은 어디까지인가요?
A: 무료 플랜에서는 디지털 명함 [N]개 생성, QR코드 생성, NFC 기본 기능을 이용할 수 있습니다. 포트폴리오 공유, 고급 템플릿 등은 프리미엄 구독이 필요합니다.

Q: 구독을 해지하면 기존 명함 데이터는 어떻게 되나요?
A: 구독 해지 후에도 무료 플랜 범위 내의 데이터는 유지됩니다. 무료 한도를 초과하는 명함은 열람은 가능하지만 수정이 제한될 수 있습니다.

Q: 결제가 중복으로 되었어요. 어떻게 해야 하나요?
A: 앱스토어(구글 플레이 / 애플 앱스토어)를 통한 결제는 각 스토어 고객센터로 문의하시면 가장 빠르게 처리됩니다. NUGGO 고객지원(support@nuggo.app)으로도 내용을 공유해 주시면 함께 확인해 드립니다.

Q: 환불은 어떻게 신청하나요?
A: 디지털 구독: 결제일로부터 7일 이내 support@nuggo.app 또는 카카오톡 채널로 문의해 주세요.

※ QR코드·명함카드 인쇄 주문 서비스는 추후 제공 예정입니다. 관련 문의는 support@nuggo.app으로 연락해 주세요.

🗂️ 포트폴리오 공유

Q: 포트폴리오 URL을 변경할 수 있나요?
A: 포트폴리오 설정 화면에서 고유 URL 주소(슬러그)를 변경할 수 있습니다. 변경 시 기존 URL은 비활성화됩니다.

Q: 포트폴리오가 검색엔진에 노출되지 않게 하려면 어떻게 하나요?
A: 포트폴리오 공개 설정을 '전체 공개' 대신 '링크 공유만 허용'으로 변경하면 검색엔진에 인덱싱되지 않습니다. [포트폴리오 설정] → [공개 범위] → [링크 공유]로 변경하세요.

Q: 탈퇴하면 공개된 포트폴리오 URL은 어떻게 되나요?
A: 탈퇴 즉시 공개 URL이 비활성화되어 외부에서 접근할 수 없게 됩니다. 단, 검색엔진에 캐싱된 정보는 검색엔진 측의 색인 갱신 주기에 따라 일정 시간 후 사라집니다.

🔧 오류 및 문제 해결

Q: 앱이 자꾸 종료되거나 로딩이 느려요.
A: ① 앱을 완전히 종료 후 재실행 → ② 기기를 재시작 → ③ 앱 업데이트 확인 → ④ 앱 삭제 후 재설치 순으로 시도해 보세요. 증상이 지속되면 기기 모델과 OS 버전을 알려주시고 고객지원으로 문의해 주세요.

Q: NFC 인식이 안 돼요.
A: ① 기기 NFC 설정이 활성화되어 있는지 확인 → ② NFC 태그를 기기 뒷면 중앙(카메라 근처)에 밀착 → ③ 보호 케이스를 제거하고 시도 → ④ 다른 NFC 태그로 테스트해 보세요.

Q: QR코드 스캔이 안 돼요.
A: QR코드 이미지가 충분히 밝고 선명한 화면에 표시되어 있는지 확인하세요. 카메라 앱 또는 기본 QR 스캔 앱으로 스캔해도 인식이 안 되면 QR코드를 새로 생성해 보세요.

Q: 이메일 인증 메일이 오지 않아요.
A: 스팸 메일함을 먼저 확인해 주세요. 그래도 없다면 [인증 메일 재발송] 버튼을 눌러 다시 시도하거나, 가입 시 입력한 이메일 주소가 정확한지 확인해 주세요.

