디지털 명함 · QR코드 · 포트폴리오 공유 · 명함카드 인쇄 서비스 적용

NUGGO(이하 '회사')는 「개인정보 보호법」, 「전자상거래 등에서의 소비자보호에 관한 법률」, 「통신비밀보호법」 등 관련 법령을 준수하며 이용자의 개인정보를 소중히 보호합니다. 본 방침은 회사가 제공하는 디지털 명함 서비스(이하 '서비스') 이용과 관련하여 개인정보의 수집·이용·제공·보관·파기 등 처리 기준을 안내합니다.

제1조 (개인정보의 처리 목적)

회사는 다음 목적을 위하여 개인정보를 처리합니다. 처리한 개인정보는 아래 목적 이외의 용도로 이용되지 않으며, 목적이 변경될 경우 별도 동의를 받겠습니다.
회원 가입 및 관리: 본인 확인, 회원 식별, 부정 이용 방지
서비스 제공 및 계약 이행: 디지털 명함 생성·관리, QR코드 생성·인쇄, NFC 연동, 포트폴리오 공개·공유, 명함카드 실물 인쇄 주문 및 배송
유료 서비스 결제 및 구독 관리: 결제 처리, 구독 관리, 환불 처리
고객 상담 및 민원 처리: 문의 응대, 불만 및 고충 처리
서비스 개선 및 통계 분석: 이용 현황 분석, 신규 서비스 개발 및 품질 향상
마케팅 및 광고성 정보 제공 (별도 동의 시): 이벤트·혜택 안내, 맞춤형 서비스 추천

제2조 (수집하는 개인정보 항목)

① 회원 가입 시 필수 항목
이메일 주소, 비밀번호, 이름 또는 닉네임, 프로필 사진

② 서비스 이용 중 선택 항목
직업, 회사명, 연락처, SNS 링크, 웹사이트 URL
포트폴리오 항목: 경력사항, 학력, 작업물 이미지, 외부 링크(GitHub, Behance 등)
기타 회원이 명함·포트폴리오에 직접 입력하는 정보

③ 유료 서비스 결제 시
결제 수단 정보(신용카드 번호, 유효기간 등) — 카드 정보는 결제대행사(PG사)에서 직접 처리하며 회사는 저장하지 않습니다.
결제 내역, 구독 이력

④ 명함카드 실물 인쇄 주문 시
수령인 이름, 배송 주소(우편번호 포함), 수령인 연락처
인쇄용 명함 디자인 데이터(이름, 직함, 연락처, 회사명 등)

⑤ 서비스 이용 과정에서 자동 수집되는 항목
IP 주소, 기기 고유 식별자(Device ID), 광고 식별자(ADID/IDFA)
OS 버전, 앱 버전, 브라우저 정보
서비스 이용 기록, 접속 일시, 불량 이용 기록

⑥ 기기 접근 권한
카메라 / 갤러리: 프로필·명함·포트폴리오 이미지 업로드
NFC 권한: NFC 데이터 쓰기 및 명함 전송

