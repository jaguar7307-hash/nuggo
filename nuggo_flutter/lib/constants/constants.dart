검색엔진 인덱싱: 공개 URL은 구글, 네이버 등 검색엔진에 수집·노출될 수 있습니다. 검색엔진 노출을 원하지 않는 경우 '비공개' 또는 '링크 공유만 허용' 설정을 이용하시기 바랍니다.
